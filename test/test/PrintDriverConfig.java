    public void printText(JTextArea txtShow) {
        int SIZE = MediaPrintableArea.INCH;
        float width, height;

        int sizePage = jComboBox1.getSelectedIndex();
        if (sizePage == 1) {
            SIZE = MediaPrintableArea.MM;
        }

        if (jRadioButton1.isSelected()) {
            width = 160f;
            height = 144f;
        } else if (jRadioButton2.isSelected()) {
            width = 80f;
            height = 72f;
        } else {
            try {
                width = Float.parseFloat(txtWidth.getText());
                height = Float.parseFloat(txtHeight.getText());
            } catch (NumberFormatException e) {
                width = 80f;
                height = 72f;
            }
        }
        
        System.out.println(txtShow.getText());
        JEditorPane paneText = new JEditorPane("text/html", txtShow.getText());
        paneText.setBackground(null);
        try {
            HashPrintRequestAttributeSet attr = new HashPrintRequestAttributeSet();
            attr.add(new MediaPrintableArea(0f, 0f, width, height, SIZE));

            paneText.print(null, null, false, getPrinter(), attr, false);
        } catch (PrinterException ex) {
            System.out.println(ex.getMessage());
        }
    }

    private void loadPrinterAll() {
        PrintService[] printService = PrinterJob.lookupPrintServices();
        
        for (PrintService printService1 : printService) {
            cbPrinterAll.addItem(printService1.getName());
        }
    }
    
    private PrintService getPrinter() {
        PrintService[] printService = PrinterJob.lookupPrintServices();

        for (PrintService printService1 : printService) {
            if (printService1.getName().equals(cbPrinterAll.getItemAt(cbPrinterAll.getSelectedIndex()))) {
                return printService1;
            }
        }

        return printService[0];
    }
    
