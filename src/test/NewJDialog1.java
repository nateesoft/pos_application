    private JPanel getPanelButton(String tableNo, String customer, Color bgColor, Color cTable, Color cCustomer) {

        Color bg = new Color(0, 204, 204);
        if (bgColor != null) {
            bg = bgColor;
        }

        Color tb = new Color(51, 0, 153);
        if (cTable != null) {
            tb = cTable;
        }

        Color cust = new Color(255, 255, 255);
        if (cCustomer != null) {
            cust = cCustomer;
        }

        //button1
        JLabel lbTable = new JLabel("");
        lbTable.setFont(new Font("Tahoma", 1, 48));
        lbTable.setForeground(tb);
        lbTable.setHorizontalAlignment(SwingConstants.CENTER);
        lbTable.setText("โต๊ะ " + tableNo);
        lbTable.setBorder(new SoftBevelBorder(BevelBorder.RAISED));

        JLabel lbCustomer = new JLabel("");
        lbCustomer.setFont(new Font("Tahoma", 1, 24));
        lbCustomer.setForeground(cust);
        lbCustomer.setHorizontalAlignment(SwingConstants.CENTER);
        lbCustomer.setText("ลูกค้า " + customer + " ท่าน");
        lbCustomer.setBorder(new SoftBevelBorder(BevelBorder.RAISED));

        JLabel lbImage = new JLabel("");
        lbImage.setIcon(new ImageIcon(getClass().getResource("/images/table.png")));

        JPanel pn = new JPanel();
        pn.setBackground(bg);
        pn.setBorder(new SoftBevelBorder(BevelBorder.RAISED));
        pn.setLayout(new AbsoluteLayout());
        pn.add(lbTable, new AbsoluteConstraints(20, 10, 230, 70));
        pn.add(lbCustomer, new AbsoluteConstraints(20, 120, 230, 40));
        pn.add(lbImage, new AbsoluteConstraints(0, 10, 250, 170));

        return pn;
    }
